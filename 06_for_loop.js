//Numbers between 1 to 10
console.log("Numbers between 1 to 10")
for(let i=1;i<=10;i++)
    console.log(i)

//Numbers from 1 to 10 incremented by 2
console.log("Numbers from 1 to 10 incremented by 2")
for(let j=1;j<=10;j=j+2)
    console.log(j)

//Numbers from 1 to 10 in reverse order
console.log("Numbers between 1 to 10 in reverse order")
for(let k=10;k>=1;k--)
console.log(k)
   
